// 测试多维数组初始化
int main() {
    // 完整初始化
    int a[2][3] = {{1,2,3},{4,5,6}};
    // 部分初始化（其余元素为 0）
    int b[2][3] = {{10},{20,25}};

    // a[0][0]=1, a[0][1]=2, a[0][2]=3, a[1][0]=4, a[1][1]=5, a[1][2]=6
    // b[0][0]=10, b[0][1]=0, b[0][2]=0, b[1][0]=20, b[1][1]=25, b[1][2]=0
    return a[0][0] + a[1][2] + b[0][0] + b[1][1] + b[0][2] + b[1][2];
    // 1 + 6 + 10 + 25 + 0 + 0 = 42
}
