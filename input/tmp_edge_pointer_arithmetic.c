// 测试指针算术边界情况
// 预期退出码：42

int main() {
    int arr[5] = {10, 20, 30, 40, 50};
    int *p = arr;

    // 指针递增
    p = p + 1;
    int a = *p;  // 20

    // 指针递减
    p = p - 1;
    int b = *p;  // 10

    // 指针加法
    int *q = arr + 3;
    int c = *q;  // 40

    // 通过指针修改
    *q = 42;
    int d = arr[3];  // 42

    return a + b + c + d - 70;  // 20 + 10 + 40 + 42 - 70 = 42
}
