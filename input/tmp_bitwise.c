int main() {
    int a = 60;   // 0011 1100
    int b = 13;   // 0000 1101
    int r = 0;

    // 位与 60 & 13 = 12 (0000 1100)
    r = a & b;
    if (r != 12) return 1;

    // 位或 60 | 13 = 61 (0011 1101)
    r = a | b;
    if (r != 61) return 2;

    // 位异或 60 ^ 13 = 49 (0011 0001)
    r = a ^ b;
    if (r != 49) return 3;

    // 位取反 ~0 = -1
    r = ~0;
    if (r != -1) return 4;

    // 左移 1 << 10 = 1024
    r = 1 << 10;
    if (r != 1024) return 5;

    // 右移 1024 >> 10 = 1
    r = 1024 >> 10;
    if (r != 1) return 6;

    // 取模 17 % 5 = 2
    r = 17 % 5;
    if (r != 2) return 7;

    // 负数取模 -7 % 3 = -1 (C99 规则)
    r = -7 % 3;
    if (r != -1) return 8;

    return 0;
}
